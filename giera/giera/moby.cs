using System.Text;
using System.Threading.Tasks;

namespace giera
{
    internal class Moby
    {
        public class Rycer
        {
            public string IMJIE;
            public int Hp;
            public int Damage;
            public int Coiny;
            public string[] Items;
            public int Killcount;
            public Rycer(int hp, int damage, int coins, string[] items,int killcount, string iMJIE)
            {
                IMJIE = iMJIE;
                Hp = hp;
                Damage = damage;
                Coiny = coins;
                Items = items;
                Killcount = killcount;

            }

        }

        public class DEMONPIEKELNY
        {
            public int Hp;
            public int Damage;
            public int Coins;
            public int LVL;
            public string Imje;
            public DEMONPIEKELNY(int hp,int damage,int lvl)
            {
                Hp=hp; 
                Damage = damage; 
                LVL = lvl;
                Coins = systems.rand(1,10)*LVL;
                
            }
        }

        
    }
